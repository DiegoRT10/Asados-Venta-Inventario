    private Boolean Vacio() {
        return txtNombre.getText().isEmpty() && txtCategoria.getText().isEmpty() 
                 && txtUnidad.getText().isEmpty() 
                && txtPrecioVenta.getText().isEmpty(); 
    }
    
    private Boolean EditarComida(){
    if (!Vacio()) {
            Comida c = new Comida(); 
    
    c.setId(id);
    c.setNombre(txtNombre.getText().trim());
    c.setCategoria(txtCategoria.getText().trim());
    c.setUnidad(txtUnidad.getText().trim());
    BigDecimal precioVenta = new BigDecimal(txtPrecioVenta.getText().trim());
    c.setPrecio(precioVenta);

            try {
                ComidaEntityManager.edit(c);
                return true;
            } catch (Exception ex) {
//            Logger.getLogger(FormUsuario.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Datos repetidos o mal ingresados");
                return false;
            }
        } else {
            JOptionPane.showMessageDialog(null, "Faltan campos por llenar");
        }
        return false;
    }
    
