    public static void DatosProducto(String codigo, String nombre, String categoria, String unidad,  Double precioCompra, Double precioVenta, int stock, int p) {
        DefaultTableModel model = (DefaultTableModel) tblProductos.getModel();

        //model.setRowCount(0); //eliminar filas existentes
        tblProductos.setDefaultRenderer(Object.class, new Render());
        Object newRow[] = {codigo, nombre, categoria,  unidad,  precioCompra, precioVenta, stock, precioVenta * stock, p};
        model.addRow(newRow);

        for (int i = 0; i < tblProductos.getRowCount(); i++) {
            TotalCompra += Double.parseDouble(tblProductos.getValueAt(i, 7).toString());
        }
        txtTotalPagar.setText(TotalCompra.toString());
        TotalCompra = 0.0;
    }

