    private Boolean Vacio() {
        return  txtNIT.getText().isEmpty() && txtNombre.getText().isEmpty() 
                && txtApellidos.getText().isEmpty() && txtDireccion.getText().isEmpty() 
                && txtTelefono.getText().isEmpty();
    }
    
    private Boolean EditarCliente(){
    if (!Vacio()) {
            Cliente p = new Cliente();
            p.setId(idB);
            p.setNit(txtNIT.getText().trim());
            p.setNombre(txtNombre.getText().trim());
            p.setApellidos(txtApellidos.getText().trim());
            p.setDireccion(txtDireccion.getText().trim());
            p.setTelefono(txtTelefono.getText().trim());

            try {
                ClienteEntityManager.edit(p);
                return true;
            } catch (Exception ex) {
//            Logger.getLogger(FormUsuario.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Datos repetidos o mal ingresados");
                return false;
            }
        } else {
            JOptionPane.showMessageDialog(null, "Faltan campos por llenar");
        }
        return false;
    }
    
